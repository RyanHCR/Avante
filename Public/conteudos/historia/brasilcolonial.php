    <p>O período colonial brasileiro, que se estende de 1500 a 1822, foi uma fase marcante da história do Brasil. Ele abrange desde o "descobrimento" pelos portugueses até a independência do país, passando pela construção de uma sociedade colonial baseada na exploração de recursos naturais, escravidão, e formação cultural.</p>
    
    <p>A CHEGADA DOS PORTUGUESES (1500)</p>
    <p>A história do Brasil colonial começa oficialmente em 22 de abril de 1500, quando a esquadra comandada por Pedro Álvares Cabral chegou à costa brasileira. Embora o "descobrimento" seja uma questão controversa, dado que o território já era habitado por milhões de indígenas, foi a partir desse marco que o Brasil entrou no radar das potências europeias.</p>
    <img src="../../../Resources/images/navegações1.jpg">

    <p>OBJETIVO INICIAL DOS PORTUGUESES:</p>
    <p>Exploração de riquezas naturais, como o pau-brasil, que seria a primeira grande fonte de riqueza explorada pelos colonizadores.</p>
    
    <p>INTERESSES EUROPEUS:</p>
    <p>A rota para as Índias e o comércio de especiarias eram os grandes objetivos econômicos dos portugueses. A exploração do Brasil, inicialmente, era secundária, mas isso mudou com o tempo.</p>

    <p>A COLONIZAÇÃO E O SISTEMA DE CAPITANIAS HEREDITÁRIAS (1530 - 1549)</p>
    <p>A colonização do Brasil de forma sistemática só começou na década de 1530, quando a Coroa Portuguesa passou a enfrentar ameaças estrangeiras (franceses e holandeses) e percebeu o potencial econômico do território.</p>
    <p>Capitanias Hereditárias: Em 1534, o rei Dom João III dividiu o Brasil em 15 capitanias, que foram entregues a nobres e fidalgos portugueses. O sistema falhou em grande parte, exceto pelas capitanias de São Vicente e Pernambuco.</p>
    <p>Governo-Geral: Em 1549, a Coroa criou o Governo-Geral, centralizando a administração colonial e designando Tomé de Sousa como primeiro governador-geral. Salvador foi escolhida como capital.</p>
