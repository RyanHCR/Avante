    <div class="box103">
    <p class="text102">CRISES DA IDADE MÉDIA</p>
    </div>
    <p class="text63">A Baixa Idade Média viu o surgimento de crises que abalaram a sociedade feudal:</p>

    <div class="box-container">
 <div class="box81">
    <p><b>A Peste Negra (1347-1351): Uma das pandemias mais devastadoras da história, que dizimou cerca de um terço da população europeia.</b></p>
    </div>
<div class="box91">
    <p><b>A Guerra dos Cem Anos (1337-1453): Um conflito prolongado entre a Inglaterra e a França, que contribuiu para a centralização do poder nas monarquias.</b></p>
    </div>
<div class="box101">
    <p><b>Revoltas Camponesas: Insatisfeitos com os impostos e a exploração, camponeses se rebelaram contra seus senhores em várias partes da Europa.</b></p>
    </div>
</div>
<BR></br><br>

<hr class="hr9">
<div class="box30">
    <p class="text51">CULTURA E CONHECIMENTO</p>
</div>

    <p class="text62">A cultura medieval era fortemente influenciada pela religião. Os mosteiros eram centros de produção intelectual, preservando e copiando manuscritos antigos. A partir do século XII, com a fundação das primeiras universidades (como Paris, Oxford e Bolonha), houve um renascimento do saber, especialmente nas áreas de filosofia, teologia e direito.</p>

    <p class="text64"><span class="highlight">Escolástica :</span>Corrente filosófica que buscava conciliar a fé cristã com a razão, sendo Tomás de Aquino um de seus maiores expoentes.</p>

    <div class="box103">
    <p class="text102">O FIM DA IDADE MÉDIA</p>
    </div>

    <p class="text65">O final da Idade Média foi marcado por uma série de transformações econômicas, sociais e políticas que deram origem à Idade Moderna. A queda de Constantinopla em 1453, a invenção da prensa por Gutenberg e a descoberta do Novo Mundo por Cristóvão Colombo em 1492 são marcos desse período de transição.</p>
