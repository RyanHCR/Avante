<div class="box103">
    <p class="text102">ECONOMIA FEUDAL</p></div>
    <div>
    <p class="text61">O sistema feudal foi a principal estrutura econômica da Idade Média, baseado em um modelo agrário. Os camponeses cultivavam a terra em troca de proteção e um pedaço de terra para
    subsistência. A produção era predominantemente de subsistência, e o comércio era limitado até o renascimento comercial no final da Baixa Idade Média. 
    </p>
    </div>
    <img class="image-right" src="../../../Resources/images/ECONOMIA1.jpg">
    <p class="text81">A economia feudal, inserida contexto do feudalismo, era uma economia agrária e de subsistência baseada na posse de terras (feudos). Os feudos, por sua vez, eram autossuficientes, mantendo sua produção quase que de forma exclusiva para sanar uma demanda interna. O que era produzido em um feudo, na sua grande
    maioria, era consumido ali. Isso porque o comércio acontecia através da troca de produtos, o escambo. O uso da moeda no feudalismo não era recorrente.
    </p>

    <div class="box41">
    <p class="text7">A IGREJA CATÓLICA E O PODER ESPIRITUAL</p>
    </div>
    <div class="img-text">
    <img class= "image-left" src="../../../Resources/images/IGREJA1.jpg">
    <p class="text82">A Igreja Católica era a instituição mais poderosa da Idade Média, exercendo influência não só espiritual, mas também política. Durante este período, surgiram ordens monásticas (como os beneditinos) e movimentos de reforma religiosa. A Igreja controlava o ensino, as artes e a moral da sociedade.</p>
    </div>
<br></br></br>
    <div class="box5-container">
    <hr class="hr3">
    <div class="box5">
        <p class="text10">AS CRUZADAS</p>
    </div>
    <hr class="hr4">
</div>
    
<p class="text62">As Cruzadas (1096-1291) foram expedições militares organizadas pela cristandade europeia com o objetivo de recuperar Jerusalém e outros lugares sagrados sob domínio muçulmano. Embora muitas vezes vistas como um fracasso militar, elas tiveram consequências importantes, como o aumento das trocas comerciais entre Oriente e Ocidente.</p>
    
