    <a href="../../materias/historia.php" class="button-back">VOLTAR</a>
    <p>A Idade Média é o nome do período da história localizado entre os anos 476 e 1453. A nomeação “Idade Média” é utilizada pelos historiadores dentro de uma periodização que engloba quatro idades: Antiga, Média, Moderna e Contemporânea.</p>
    <p>A Idade Média, período que se estendeu aproximadamente entre os séculos V e XV, é uma fase histórica que desperta grande interesse por sua complexidade e influência no desenvolvimento da sociedade ocidental.</p>
    <img src="../../../Resources/images/images1.jpg">

    <p>CONTEXTO HISTÓRICO</p>
    <p>A Idade Média começou após a queda do Império Romano do Ocidente (476 d.C.) e se estendeu até o início da Idade Moderna, com a chegada de eventos marcantes como a Renascença e as Grandes Navegações.</p>
    
    <p>DIVISÕES DA IDADE MÉDIA:</p>
    <p>Alta Idade Média (séculos V a X): Invasões bárbaras e formação dos reinos germânicos. Ascensão do Feudalismo e da Igreja Católica. Ocorreu uma descentralização do poder político.</p>
    <p>Baixa Idade Média (séculos XI a XV): Fortalecimento das monarquias. Crescimento populacional e urbano. Desenvolvimento do comércio e as Cruzadas.</p>
    
    <p>SOCIEDADE MEDIEVAL</p>

    <p>A sociedade medieval era estamental, ou seja, não possibilitava a ascensão social. No topo da pirâmide estava o clero, logo abaixo vinha a nobreza, e na base estavam os servos, os únicos que trabalhavam e sustentavam as classes de cima. A agricultura tornou-se a principal atividade econômica. A sociedade medieval era marcada pela hierarquia e pelo sistema feudal, no qual a relação entre senhores e vassalos dominava a organização social. Ela era dividida principalmente em três estamentos:</p>
    <img src="../../../Resources/images/piramide1.jpg">
    <p>Clero: Responsável pela vida religiosa e espiritual, tinha grande influência política e social.</p>
    <p>Nobreza: Proprietários de terras que detinham poder militar e econômico.</p>
    <p>Camponeses: A maior parte da população, vivendo sob a tutela dos senhores feudais e trabalhando nas terras em troca de proteção.</p>
    <img src="../../../Resources/images/sociedade1.jpg">

    <p>ECONOMIA FEUDAL</p>
    <p>O sistema feudal foi a principal estrutura econômica da Idade Média, baseado em um modelo agrário. Os camponeses cultivavam a terra em troca de proteção e um pedaço de terra para
    subsistência. A produção era predominantemente de subsistência, e o comércio era limitado até o renascimento comercial no final da Baixa Idade Média. 
    </p>
    <img src="../../../Resources/images/ECONOMIA1.jpg">
    <p>A economia feudal, inserida contexto do feudalismo, era uma economia agrária e de subsistência baseada na posse de terras (feudos). Os feudos, por sua vez, eram autossuficientes, mantendo sua produção quase que de forma exclusiva para sanar uma demanda interna. O que era produzido em um feudo, na sua grande
    maioria, era consumido ali. Isso porque o comércio acontecia através da troca de produtos, o escambo. O uso da moeda no feudalismo não era recorrente.
    </p>

    <p>A IGREJA CATÓLICA E O PODER ESPIRITUAL</p>
    <img src="../../../Resources/images/IGREJA1.jpg">
    <p>A Igreja Católica era a instituição mais poderosa da Idade Média, exercendo influência não só espiritual, mas também política. Durante este período, surgiram ordens monásticas (como os beneditinos) e movimentos de reforma religiosa. A Igreja controlava o ensino, as artes e a moral da sociedade.</p>
    
    <p>AS CRUZADAS</p>
    <p>As Cruzadas (1096-1291) foram expedições militares organizadas pela cristandade europeia com o objetivo de recuperar Jerusalém e outros lugares sagrados sob domínio muçulmano. Embora muitas vezes vistas como um fracasso militar, elas tiveram consequências importantes, como o aumento das trocas comerciais entre Oriente e Ocidente.</p>
    <img src="../../../Resources/images/CRUZADAS1.jpg">

    <p>CRISES DA IDADE MÉDIA</p>
    <p>A Baixa Idade Média viu o surgimento de crises que abalaram a sociedade feudal:</p>
    <p>A Peste Negra (1347-1351): Uma das pandemias mais devastadoras da história, que dizimou cerca de um terço da população europeia.</p>
    <p>A Guerra dos Cem Anos (1337-1453): Um conflito prolongado entre a Inglaterra e a França, que contribuiu para a centralização do poder nas monarquias.</p>
    <p>Revoltas Camponesas: Insatisfeitos com os impostos e a exploração, camponeses se rebelaram contra seus senhores em várias partes da Europa.</p>
    
    <p>CULTURA E CONHECIMENTO</p>
    <p>A cultura medieval era fortemente influenciada pela religião. Os mosteiros eram centros de produção intelectual, preservando e copiando manuscritos antigos. A partir do século XII, com a fundação das primeiras universidades (como Paris, Oxford e Bolonha), houve um renascimento do saber, especialmente nas áreas de filosofia, teologia e direito.</p>
    <p>Escolástica : Corrente filosófica que buscava conciliar a fé cristã com a razão, sendo Tomás de Aquino um de seus maiores expoentes.</p>
    <p>O FIM DA IDADE MÉDIA</p>
    <p>O final da Idade Média foi marcado por uma série de transformações econômicas, sociais e políticas que deram origem à Idade Moderna. A queda de Constantinopla em 1453, a invenção da prensa por Gutenberg e a descoberta do Novo Mundo por Cristóvão Colombo em 1492 são marcos desse período de transição.</p>
    
