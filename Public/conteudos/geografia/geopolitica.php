<div class="texto1">Geopolítica é uma área do conhecimento que se dedica ao estudo das relações de poder entre Estados e territórios, bem como a forma como se organizam no sistema-mundo.</div>
<div class="texto2">A geopolítica é um campo de estudo interdisciplinar que examina como fatores geográficos, econômicos e políticos influenciam as relações de poder entre as nações. Esse conceito vai além da simples análise territorial, focando nas estratégias que os países adotam para manter sua soberania, garantir acesso a recursos e se posicionar no cenário internacional. Ao estudar geopolítica, entendemos os motivos por trás de conflitos, alianças e disputas por recursos que moldam o mundo atual.</div>

<div class="imagem1"><img src="../../../Resources/images/geopolitica1.jpg"></div>

<div class="texto3">A ORIGEM DO CONCEITO</div>
<div class="texto4">O termo "geopolítica" foi usado pela primeira vez pelo cientista político sueco Rudolf Kjellén, no início do século XX. Desde então, ele evoluiu para se tornar uma ferramenta essencial para analisar a dinâmica global. Inicialmente, a geopolítica estava muito ligada às ambições imperialistas dos grandes impérios, com foco na expansão territorial e controle de áreas estratégicas. Ao longo do tempo, o conceito se expandiu, incorporando novos elementos como globalização, tecnologia e crises ambientais.</div>
<div class="imagem2"><img src="../../../Resources/images/geopolitica3.jpg"></div>
    
<div class="texto5">FATORES GEOPOLÍTICOS</div>
<div class="texto6">A geopolítica leva em conta vários fatores para analisar o comportamento dos Estados no cenário internacional:<br><br>
   &#9679;Geografia: A localização física de um país influencia suas oportunidades e desafios. Um exemplo claro disso é a Rússia, que sempre buscou acesso a mares quentes para garantir uma saída naval viável durante todo o ano. Países insulares, como o Reino Unido e o Japão, historicamente se beneficiaram de sua proteção natural contra invasões terrestres.<br><br>
   &#9679;Recursos Naturais: A presença de petróleo, gás natural, minérios ou água potável pode determinar o poder de um país. O Oriente Médio, por exemplo, é uma região chave na geopolítica global por abrigar as maiores reservas de petróleo do mundo, o que faz com que potências globais mantenham interesses estratégicos na área.<br><br>
   &#9679;Demografia: O tamanho e a composição da população também influenciam o poder geopolítico. Nações com populações jovens, como a Índia, têm a vantagem de uma força de trabalho crescente, enquanto países com populações envelhecidas, como o Japão, enfrentam desafios para manter o crescimento econômico.</div>

   <div class="texto7">GEOPOLÍTICA COMTEMPÔRANEA</div>
   <div class="texto8">Na atualidade, a geopolítica envolve muito mais do que a simples conquista de território. Com a globalização, surgiram novos elementos que redefinem as relações de poder no mundo:<br><br>
   &#9679;Globalização e Economia: As economias estão cada vez mais interligadas. O que acontece em uma parte do mundo, como uma crise financeira ou uma pandemia, pode impactar diretamente outras regiões. A China, por exemplo, emergiu como uma superpotência global devido ao seu crescimento econômico rápido e ao seu papel como a "fábrica do mundo".<br><br>
   &#9679;Tecnologia e Cibersegurança: A disputa pelo domínio tecnológico é uma nova fronteira da geopolítica. A capacidade de desenvolver e controlar tecnologias de ponta, como a inteligência artificial e as redes 5G, tornou-se uma questão estratégica para países como os Estados Unidos e China.<br><br>
   &#9679;Mudanças Climáticas: O aquecimento global trouxe novas preocupações geopolíticas. Nações estão disputando o controle de áreas do Ártico que, com o derretimento do gelo, podem revelar novos recursos minerais e rotas marítimas. Além disso, o deslocamento de populações devido a desastres naturais cria novas tensões e desafios migratórios.</div>
    
   <div class="texto9">ORGANIZAÇÕES INTERNACIONAIS E GEOPOLÍTICA</div>
   <div class="texto10">Outro aspecto crucial da geopolítica moderna são as organizações internacionais, como a ONU (Organização das Nações Unidas), a OTAN (Organização do Tratado do Atlântico Norte) e a OMC (Organização Mundial do Comércio). Essas entidades regulam ou mediam conflitos entre nações, estabelecem normas de comércio e ajudam a manter a paz em áreas de tensão. No entanto, as relações de poder dentro dessas organizações muitas vezes refletem a geopolítica, com países como os Estados Unidos, China e Rússia exercendo maior influência em algumas decisões.</div>
