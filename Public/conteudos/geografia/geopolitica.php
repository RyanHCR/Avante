    <img src="../../../Resources/images/geopolitica1.jpg">

    <p>A ORIGEM DO CONCEITO</p>
    <p>O termo "geopolítica" foi usado pela primeira vez pelo cientista político sueco Rudolf Kjellén, no início do século XX. Desde então, ele evoluiu para se tornar uma ferramenta essencial para analisar a dinâmica global. Inicialmente, a geopolítica estava muito ligada às ambições imperialistas dos grandes impérios, com foco na expansão territorial e controle de áreas estratégicas. Ao longo do tempo, o conceito se expandiu, incorporando novos elementos como globalização, tecnologia e crises ambientais.</p>
    <img src="../../../Resources/images/geopolitica3.jpg">
    
    <p>FATORES GEOPOLÍTICOS</p>
    <p>A geopolítica leva em conta vários fatores para analisar o comportamento dos Estados no cenário internacional:</p>
    <li>Geografia: A localização física de um país influencia suas oportunidades e desafios. Um exemplo claro disso é a Rússia, que sempre buscou acesso a mares quentes para garantir uma saída naval viável durante todo o ano. Países insulares, como o Reino Unido e o Japão, historicamente se beneficiaram de sua proteção natural contra invasões terrestres.</li>
    <li>Recursos Naturais: A presença de petróleo, gás natural, minérios ou água potável pode determinar o poder de um país. O Oriente Médio, por exemplo, é uma região chave na geopolítica global por abrigar as maiores reservas de petróleo do mundo, o que faz com que potências globais mantenham interesses estratégicos na área.</li>
    <li>Demografia: O tamanho e a composição da população também influenciam o poder geopolítico. Nações com populações jovens, como a Índia, têm a vantagem de uma força de trabalho crescente, enquanto países com populações envelhecidas, como o Japão, enfrentam desafios para manter o crescimento econômico.</li>

    <p>GEOPOLÍTICA COMTEMPÔRANEA</p>
    <p>Na atualidade, a geopolítica envolve muito mais do que a simples conquista de território. Com a globalização, surgiram novos elementos que redefinem as relações de poder no mundo:</p>
    <li>Globalização e Economia: As economias estão cada vez mais interligadas. O que acontece em uma parte do mundo, como uma crise financeira ou uma pandemia, pode impactar diretamente outras regiões. A China, por exemplo, emergiu como uma superpotência global devido ao seu crescimento econômico rápido e ao seu papel como a "fábrica do mundo".</li>
    <li>Tecnologia e Cibersegurança: A disputa pelo domínio tecnológico é uma nova fronteira da geopolítica. A capacidade de desenvolver e controlar tecnologias de ponta, como a inteligência artificial e as redes 5G, tornou-se uma questão estratégica para países como os Estados Unidos e China.</li>
    <li>Mudanças Climáticas: O aquecimento global trouxe novas preocupações geopolíticas. Nações estão disputando o controle de áreas do Ártico que, com o derretimento do gelo, podem revelar novos recursos minerais e rotas marítimas. Além disso, o deslocamento de populações devido a desastres naturais cria novas tensões e desafios migratórios.</li>
    
    <p>ORGANIZAÇÕES INTERNACIONAIS E GEOPOLÍTICA</p>
    <p>Outro aspecto crucial da geopolítica moderna são as organizações internacionais, como a ONU (Organização das Nações Unidas), a OTAN (Organização do Tratado do Atlântico Norte) e a OMC (Organização Mundial do Comércio). Essas entidades regulam ou mediam conflitos entre nações, estabelecem normas de comércio e ajudam a manter a paz em áreas de tensão. No entanto, as relações de poder dentro dessas organizações muitas vezes refletem a geopolítica, com países como os Estados Unidos, China e Rússia exercendo maior influência em algumas decisões.</p>
    <img src="../../../Resources/images/geopolitica2.jpg">

    <p>CONFLITOS GEOPOLÍTICOS</p>
    <p>Alguns dos principais conflitos geopolíticos da atualidade envolvem:</p>
    <p>Conflito Rússia-Ucrânia: Desde 2014, com a anexação da Crimeia pela Rússia, as tensões na região se intensificaram. A localização estratégica da Ucrânia e seu papel como corredor de gás natural para a Europa fazem com que este conflito tenha impactos globais.</p>
    <p>Disputas no Mar do Sul da China: A China reivindica grande parte do Mar do Sul da China, uma região rica em recursos naturais e de importância estratégica para o comércio global. Isso gera tensões com outros países do sudeste asiático e com os Estados Unidos.</p>
    <p>Crise Migratória e Refugiados: Conflitos como a guerra na Síria e instabilidades políticas na Venezuela criaram crises migratórias, forçando países a lidar com questões humanitárias e segurança de fronteiras.</p>
