    <div class="texto10">INDICADORES ECONÔMICOS</div>
    <div class="texto11">Para medir o desempenho da economia, os economistas utilizam diversos indicadores. Alguns dos mais importantes são:</div>
    <div class="texto12"> <span class="bold">Produto Interno Bruto (PIB):</span> O PIB é a soma de todos os bens e serviços finais produzidos em um país durante um determinado período. Ele é o principal indicador da atividade econômica de um país. Um PIB crescente indica que a economia está se expandindo, enquanto um PIB em queda sugere recessão.<br><br>
    <span class="bold">Inflação:</span> A inflação é o aumento generalizado dos preços dos bens e serviços. Quando a inflação é muito alta, o poder de compra da população diminui, ou seja, com o mesmo dinheiro, as pessoas conseguem comprar menos.<br><br>
    <span class="bold">Taxa de Desemprego:</span> Este indicador mostra a porcentagem da força de trabalho que está sem emprego. Altas taxas de desemprego indicam problemas na economia, como falta de investimentos ou crises econômicas.<br><br>
    <span class="bold">Taxa de Juros: </span>A taxa de juros é o custo do crédito. Quando a taxa de juros está alta, o consumo e o investimento tendem a cair, pois fica mais caro pegar empréstimos. Quando está baixa, o consumo aumenta, estimulando o crescimento econômico.</div>

    <div class="texto13">GLOBALIZAÇÃO E A ECONOMIA</div>
    <div class="barra1">-</div>
    <div class="barra2">-</div>
    <div class="texto14">Nos últimos 50 anos, a economia mundial passou por um processo de globalização, no qual países, mercados e pessoas se tornaram mais interligados. Isso impactou diretamente as economias nacionais, que passaram a depender mais do comércio internacional, dos investimentos estrangeiros e das trocas culturais e tecnológicas.<br><br>
    Com a globalização, as empresas multinacionais se espalharam pelo mundo, produzindo em diferentes países para reduzir custos e aumentar seus mercados. A China, por exemplo, se tornou a "fábrica do mundo", exportando produtos para todos os continentes.<br><br>
    Ao mesmo tempo, a globalização criou desafios, como o aumento da concorrência internacional, a maior vulnerabilidade a crises econômicas globais (como a crise financeira de 2008) e a desigualdade entre países ricos e pobres.</div>
    <div class="barra3">-</div>
    <div class="texto24">DESAFIOS ECONÔMICOS ATUAIS</div>
    <div class="texto6">Em um mundo em constante mudança, a economia enfrenta desafios complexos, como:</div>
    <div class="texto25"><span class="bold">1. Desigualdade Social: </span>Apesar do crescimento econômico global, a concentração de renda nas mãos de poucos se intensificou, especialmente nas grandes economias. A desigualdade de renda entre ricos e pobres continua sendo um dos maiores desafios, e a solução para isso exige políticas públicas redistributivas e acesso igualitário à educação e saúde.</div>
    <div class="imagem11"><img src="../../../Resources/images/economia6.jpg"></div>
    <div class="texto25"><span class="bold">2. Desemprego Tecnológico:</span>Com o avanço da automação e da inteligência artificial, muitos empregos tradicionais estão desaparecendo. Esse fenômeno levanta a questão de como as sociedades vão lidar com o desemprego tecnológico, garantindo que os trabalhadores se adaptem às novas demandas do mercado.</div>
    <div class="imagem12"><img src="../../../Resources/images/economia7.jpg"></div>
    <div class="texto25"><span class="bold">3. Sustentabilidade:</span> O crescimento econômico vem acompanhado de altos custos ambientais. A exploração excessiva dos recursos naturais, o desmatamento e as mudanças climáticas são desafios que a economia global precisa enfrentar para garantir um desenvolvimento sustentável. O conceito de economia verde e economia circular surge como alternativas para equilibrar o crescimento econômico com a preservação do meio ambiente.</div>
    <div class="imagem16"><img src="../../../Resources/images/economia8.jpg"></div>
    <div class="texto27"><span class="bold">4. Crises Econômicas:</span> As economias estão sujeitas a crises, como recessões e crises financeiras. A pandemia da COVID-19, por exemplo, gerou uma das maiores crises econômicas do século XXI, evidenciando a vulnerabilidade dos mercados globais a eventos inesperados.</div>
