    <div class="texto5">OS FATORES DE PRODUÇÃO</div>
    <div class="texto6">Para entender o funcionamento da economia, é importante conhecer os fatores de <br> produção, ou seja, os recursos necessários para produzir bens e serviços. São eles:</div>
    
    <div class="container1">
    <div class="divisao1">-</div>
    <div class="texto15">TERRA</div>
    <div class="divisao2">-</div>
    </div>

    <div class="texto6">Refere-se aos recursos naturais, como solo, água, florestas, minérios e energia. Sem <br> esses recursos, não há como produzir alimentos, matérias-primas ou gerar energia.</div>
    <div class="linha1">-</div>
    <div class="texto15">TRABALHO</div>
    <div class="texto6">É a força humana aplicada na produção de bens e serviços. O trabalho pode ser <br>manual ou intelectual, e sua qualidade depende do nível de educação, habilidades e<br> saúde da população.</div>
    <div class="linha2">-</div>
    <div class="texto15">CAPITAL</div>
    <div class="texto6">Inclui máquinas, ferramentas, edifícios e infraestrutura utilizados na produção.<br> Diferente da "riqueza" no sentido de dinheiro, o capital refere-se aos bens produzidos<br> que ajudam na produção de outros bens.</div>
    <div class="linha3">-</div>
    <div class="texto6">Cada sociedade adota um sistema econômico para organizar a produção e distribuição <br>de bens e serviços. Os principais sistemas são:</div>
    <div class="texto16">TECNOLOGIA E INOVAÇÃO</div>
    <div class="texto6">Refere-se ao conhecimento aplicado na produção, melhorando a eficiência e a <br>qualidade dos produtos e serviços.</div>
    <div class="linha3">-</div>
    <div class="texto17">SISTEMAS ECONÔMICOS</div>
    <div class="texto6">Cada sociedade adota um sistema econômico para organizar a produção e distribuição<br> de bens e serviços. Os principais sistemas são:</div>
    <div class="texto18">ECONOMIA DE MERCADO</div>
    <div class="texto19">Nesse sistema, a produção e o consumo são regulados pelas leis da oferta e da demanda, com pouca interferência do governo. As decisões sobre o que produzir, como produzir e para quem produzir são determinadas pelo mercado. O capitalismo é o principal exemplo de economia de mercado. Nos mercados livres, o preço é um mecanismo que coordena as ações de produtores e consumidores.</div>
    <div class="imagem8"><img src="../../../Resources/images/economia2.jpg"></div>
    <div class="texto20">ECONOMIA PLANIFICADA</div>
    <div class="texto21">Também chamada de economia centralmente planejada, nesse sistema, o governo controla a produção e a distribuição de bens e serviços. O Estado decide o que será produzido, em que quantidade e como será distribuído. Esse sistema é comum em regimes socialistas, como na antiga União Soviética.</div>
    <div class="imagem9"><img src="../../../Resources/images/economia3.jpg"></div>
    <div class="texto22">ECONOMIA MISTA</div>
    <div class="texto23"> A maioria dos países adota um sistema econômico misto, no qual coexistem setores de mercado livre e setores controlados pelo governo. O Brasil, por exemplo, tem uma economia mista, com empresas privadas e estatais, e o governo intervém na economia por meio de políticas fiscais e monetárias.</div>
    <div class="imagem10"><img src="../../../Resources/images/economia4.jpg"></div>

    <div class="texto7">-</div>

    <div class="texto8">O PAPEL DO GOVERNO NA ECONOMIA</div>
    <div class="texto9">O governo desempenha um papel crucial na economia, atuando em diferentes áreas para garantir o equilíbrio econômico e promover o bem-estar da população. Entre as principais funções do governo estão:<br><br>
    &#9679;Política Fiscal: Diz respeito à maneira como o governo arrecada e gasta recursos. Através da política fiscal, o governo coleta impostos e decide como investir em áreas como saúde, educação e infraestrutura. O objetivo é promover o crescimento econômico, redistribuir renda e fornecer bens públicos essenciais.<br><br>
    &#9679;Política Monetária: Controlada pelos bancos centrais, essa política regula a quantidade de dinheiro em circulação e as taxas de juros, influenciando o nível de consumo, investimento e a inflação. O Banco Central do Brasil, por exemplo, ajusta as taxas de juros para controlar a inflação e estimular ou desacelerar a economia.<br><br>
    &#9679;Regulação e Legislação: O governo também estabelece regras para garantir que o mercado funcione de maneira justa e eficiente. Isso inclui leis trabalhistas, normas ambientais e regulamentações de setores estratégicos como telecomunicações e energia.</div>
