                <div class="middle-box">
                    <span>AS FORMAS DE PODER</span>
                </div>
                <div class="box"></div>
            </div>
        </body2>
        <p style="text-align: center;">O poder assume diversas formas, que podem ser classificadas de acordo com seus meios de ação e efeitos sociais:</p>
        <div class="container-grid">
            <div class="text-box tamanho3">
                <div class="number-circle">1</div>
                <p>PODER COERCITIVO</p>
                <p>1. Caracteriza-se pelo uso ou ameaça da força física para impor a obediência. Estados autoritários, por exemplo, frequentemente recorrem a esse tipo de poder para manter a ordem social.</p>

            </div>
            <div class="text-box tamanho3">
                <div class="number-circle">2</div>
                <p>PODER PERSUASIVO</p>
                <p>2. Refere-se à capacidade de influenciar por meio do discurso, da argumentação ou do convencimento. Em democracias, o poder persuasivo é a base dos processos eleitorais e da construção de consensos políticos.</p>

            </div>
            <div class="text-box tamanho3">
                <div class="number-circle">3</div>
                <p>PODER ECONÔMICO</p>
                <p>3. Está relacionado ao controle de recursos financeiros e produtivos. Grandes corporações, bancos e agentes do mercado detêm uma forma de poder que pode influenciar diretamente decisões políticas.</p>

            </div>
            <div class="text-box tamanho3">
                <div class="number-circle">4</div>
                <p>PODER SIMBÓLICO</p>
                <p>4. Estabelece-se por meio de símbolos, crenças, tradições e ideologias. Religiões, culturas e movimentos sociais exercem esse poder ao moldar valores e normas de comportamento.</p>

            </div>
        </div>
        <div class="align">
            <div class="linha2"></div>
            <div class="middle-box2">
                <span>O ESTADO: ORIGENS E DEINIÇÕES</span>
            </div>
        </div>

        <div class="align">
            <p class="text">O Estado é uma entidade que exerce o poder sobre um determinado território e população. Ele possui três elementos básicos: território, governo e população, e sua função é organizar e regular as relações dentro da sociedade, impondo a ordem e provendo o bem-estar público.</p>
        </div>
        <div class="align">
            <div class="tamanho">
                <div class="container4">
                    <div class="tab">Contratualismo</div>
                    <div class="content">
                        Pensadores como Thomas Hobbes, John Locke e Jean-Jacques Rousseau propuseram que o Estado surge de um contrato social, onde os indivíduos cedem parte de sua liberdade em troca de proteção e ordem.
                    </div>
                </div>
            </div>
        </div>
        <div class="align">
            <div class="tamanho">
                <div class="container4">
                    <div class="tab">Estado Moderno:</div>
                    <div class="content">
                        O Estado como o conhecemos hoje se consolidou a partir do fim da Idade Média, com o advento do absolutismo e, posteriormente, do Estado-Nação, após a Paz de Westfália (1648).
                    </div>
                </div>
            </div>
        </div>
        <br>
        <br>
        <div class="align">
            <div class="linha3"></div>
            <div class="middle-box3">
                <span>FUNÇÕES DO ESTADO</span>
            </div>
        </div>
        <div class="container-grid2">
            <div class="text-box3 tamanho3" style="background-color: #B5F5FF">
                <p>Legislativa: Criação de leis que regulam a sociedade.</p>
            </div>

            <div class="text-box3 tamanho3" style="background-color: #6FC3D0">
                <p>Executiva: Implementação das leis e administração pública.</p>
            </div>

            <div class="text-box3 tamanho3" style="background-color: #2F91A0">

                <p>Judiciária: Interpretação das leis e resolução de conflitos.</p>
            </div>
        </div>
        <br>
        <br>
        <div class="linha"></div><br>
        <p class="top tamanho">FORMAS DE ESTADO:</p>
        <br>

        <head>
            <div class="container5">
                <div class="box2">
                    <span>1</span>
                    <p><strong>Estado Democrático:</strong> Baseado na soberania popular, com participação direta ou indireta dos cidadãos (ex.: repúblicas e monarquias parlamentares).</p>
                </div>

                <div class="box2">
                    <span>2</span>
                    <p><strong>Estado Autoritário:</strong> Controle concentrado nas mãos de um líder ou grupo, com pouca ou nenhuma participação popular.</p>
                </div>

                <div class="box2">
                    <span>3</span>
                    <p><strong>Estado Totalitário:</strong> Controle absoluto sobre todas as esferas da vida social, política e econômica.</p>
                </div>
            </div>



        </head>

