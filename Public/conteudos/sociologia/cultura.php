    <p>SOCIOLOGIA DA CULTURA</p>
    <p>A sociologia da cultura estuda como a cultura é criada, mantida e transformada nas sociedades. Sociólogos como Émile Durkheim, Max Weber e Pierre Bourdieu contribuíram significativamente para o entendimento de como a cultura molda e é moldada pelas estruturas sociais e relações de poder.</p>
