    <p>A cultura na sociologia representa o conjunto de saberes e tradições de um povo. Estes são produzidos pela interação social entre os indivíduos de uma comunidade ou sociedade.</p>
    <p>É o conjunto de crenças, valores, costumes, artefatos, leis e normas de uma sociedade. Esse conceito compreende todos os aspectos decorrentes do contato social entre os indivíduos e reflete seu modo de vida. Todos os elementos que compõe uma cultura são resultado da criação humana e são pertencentes a uma sociedade ou a um grupo social. A cultura reflete a forma como as pessoas agem, pensam, se expressam e como reproduzem suas vidas.</p>

    <p>PRINCIPAIS CONCEITOS DE CULTURA NA SOCIOLOGIA:</p>
    <li>Cultura Material: Refere-se aos objetos físicos, como ferramentas, vestimentas, tecnologia, e infraestrutura, que são criados e utilizados por uma sociedade.</li>
    <li>Cultura Imaterial: Engloba valores, crenças, normas, tradições, mitos e sistemas de símbolos, como a linguagem, que guiam o comportamento e a interação social.</li>
    <li>Normas: Regras e expectativas de comportamento social. Elas podem ser formais (leis) ou informais (costumes e tradições).</li>
    <li>Valores: Princípios fundamentais que orientam o comportamento e as decisões de uma sociedade, como liberdade, igualdade e solidariedade.</li>
    <li>Subcultura: Um grupo dentro da sociedade que tem seu próprio conjunto de normas, valores e crenças, que podem diferir, mas coexistem com a cultura dominante.</li>
    <li>
    Etnocentrismo: Tendência de julgar outras culturas com base nos padrões da própria cultura.</li>
    <li>Relativismo Cultural: A perspectiva de que as culturas devem ser compreendidas em seus próprios termos, sem julgamentos baseados em valores externos.</li>
