    <p>A Filosofia Antiga corresponde ao período do surgimento da filosofia grega no século VII a.C. Ela surge da necessidade de explicar o mundo de um novo modo. Os filósofos buscam encontrar respostas racionais para a origem das coisas, dos fenômenos da natureza, da existência e da racionalidade humana.</p>
    <img src="../../../Resources/images/antiga1.jpeg">
    <p>A filosofia antiga surge com a substituição do saber mítico ao da razão e isso ocorreu com o surgimento da polis grega (cidade-estado). Essa nova organização grega, foi fundamental para a desmistificação do mundo através da razão e, com isso, as reflexões dos filósofos. Mais tarde, as discussões que ocorriam em praça pública juntamente com o poder da palavra e da razão (logos) levaram a criação da democracia.</p>

    <p>PRÉ-SOCRÁTICOS</p>
    <li>Foco: Esses primeiros filósofos buscavam explicações naturais para a origem do universo (cosmogonia) e a natureza das coisas.</li>
    <li>Filósofos principais: Tales de Mileto, Anaximandro, Heráclito, Parmênides.</li>
    <li>Ideias centrais: Elementos primordiais (água, fogo, ar, etc.), mudança e permanência no cosmos.</li>

    <p>SÓCRATES (469–399 a.C.)</p>
    <li>Foco: A virtude e o conhecimento. Sócrates é famoso por sua máxima "Só sei que nada sei" e pelo uso da maiêutica, um método de questionamento para atingir o conhecimento.</li> 
    <li>Legado: Não deixou escritos; suas ideias foram registradas por discípulos, como Platão.</li>
    <img src="../../../Resources/images/antiga2.jpeg">

    <p>PLATÃO (427–347 a.C.)</p>
    <li>Foco: A teoria das ideias ou formas, na qual o mundo físico é apenas uma sombra de uma realidade ideal e perfeita.</li>
    <li>Obra principal: "A República", onde discute a justiça, o governo ideal e a educação.</li>
    <li>Conceitos: Dualismo entre corpo e alma, teoria do conhecimento.</li>
    <img src="../../../Resources/images/antiga3.jpeg">

    <p>ARISTÓTELES (384–322 a.C.)</p>
    <li>Foco: Desenvolveu uma abordagem mais empírica e lógica do conhecimento, divergindo de Platão.</li>
    <li>Obras principais: "Ética a Nicômaco", "Política", "Metafísica".</li>
    <li>Ideias centrais: Teoria das causas, ética baseada na virtude, ideia de que o homem é um "animal político"</li>
    <img src="../../../Resources/images/antiga4.jpeg">

    <p>ESCOLAS HELENÍSTICAS</p>
    <li>Epicurismo: Fundado por Epicuro, defendia que a felicidade (ataraxia) era atingida pela busca de prazeres moderados e pela ausência de dor.</li>
    <li>Estoicismo: Fundado por Zenão de Cítio, enfatizava a razão, autocontrole e a aceitação das circunstâncias naturais.</li>
    <li>Ceticismo: Defendido por Pirro de Élis, argumentava que o conhecimento absoluto é impossível, promovendo uma atitude de suspensão de julgamento.</li>
    <img src="../../../Resources/images/antiga5.jpeg">

    <p>NEOPLATONISMO</p>
    <li>Foco: Desenvolvido por Plotino, o neoplatonismo reinterpretou Platão, enfatizando a ideia do Uno, de onde tudo deriva, e a busca pela união mística com essa realidade transcendente.</li>
