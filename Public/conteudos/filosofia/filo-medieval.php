<div class="box-container">
    <div class="box8">
        <p><b>   Santo Agostinho (354–430) </b><br><br> <li>Desenvolveu uma síntese entre o pensamento cristão e a filosofia de Platão.<li>Trabalhou questões como a natureza do mal, a relação entre fé e razão, e a predestinação.<li>Enfatizou a necessidade da graça divina para a salvação, sendo uma figura central na teologia cristã.</p>
    </div>
    <div class="box9">
        <p><b>      São Tomás de Aquino (1225– 1274) </b><br><br> <li>Tentou conciliar a razão e a fé, defendendo que ambas são complementares.<li>Formulou as "cinco vias" para demonstrar a existência de Deus, usando argumentos racionais.<li>Desenvolveu uma metafísica baseada na distinção entre essência e existência, e influenciou grandemente a teologia católica.</p>
    </div>
    <div class="box10">
        <p><b>      Anselmo de Cantuária (1033–1109) </b><br><br> <li>Formulou o argumento ontológico para provar a existência de Deus, que diz que Deus, sendo o ser mais perfeito concebível, deve existir na realidade e não apenas na mente.<li>Defendeu a doutrina da satisfação, onde Cristo, através de seu sacrifício, satisfaz a justiça divina.</p>
    </div>
